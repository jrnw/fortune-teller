/* Returns a list with randomly picked emojis */
export const getRandomEmojis = (count: number) => {
  let emojiResult: string[] = []
  for (let i = 0; i < count; i++) {
    emojiResult.push(emojis[Math.floor(Math.random() * emojis.length)])
  }
  return emojiResult
}
